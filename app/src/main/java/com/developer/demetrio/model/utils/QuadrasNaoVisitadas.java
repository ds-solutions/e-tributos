package com.developer.demetrio.model.utils;

public class QuadrasNaoVisitadas {
    private long totalQuadra;
    private String setor;
    private String quadra;

    public long getTotalQuadra() {
        return totalQuadra;
    }

    public void setTotalQuadra(long totalQuadra) {
        this.totalQuadra = totalQuadra;
    }

    public String getSetor() {
        return setor;
    }

    public void setSetor(String setor) {
        this.setor = setor;
    }

    public String getQuadra() {
        return quadra;
    }

    public void setQuadra(String quadra) {
        this.quadra = quadra;
    }
}
